# Using bool() some more

test = 0
print(test, ":", bool(test))

test = [1]
print(test, ":", bool(test))

test = []
print(test, ":", bool(test))


test = 1.1
print(test, ":", bool(test))

test = "1"
print(test, ":", bool(test))

test = False
print(test, ":", bool(test))

test = True
print(test, ":", bool(test))

test = "Easy string"
print(test, ":", bool(test))

test = ""
print(test, ":", bool(test))

test = {}
print(test, ":", bool(test))
