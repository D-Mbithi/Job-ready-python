# Using math module functions

import math  # Math module must be imported before using

a = 6.1
print(a)

"""The integral part of the float. """
b = math.trunc(a)
print(b)


"""Round down a. """
c = math.floor(a)
print(c)

"""Round up a. """
d = math.ceil(a)
print(d)

"""The value of pi."""
print(math.pi)
